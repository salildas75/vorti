package com.example.salildas.vorti;

public class University {

    private String varsityImage;
    private String varsityName;
    private String varsityType;
    private String getVarsityDesc;

    public University(){}

    public String getVarsityImage() {
        return varsityImage;
    }

    public void setVarsityImage(String varsityImage) {
        this.varsityImage = varsityImage;
    }

    public String getVarsityName() {
        return varsityName;
    }

    public void setVarsityName(String varsityName) {
        this.varsityName = varsityName;
    }

    public String getVarsityType() {
        return varsityType;
    }

    public void setVarsityType(String varsityType) {
        this.varsityType = varsityType;
    }

    public String getGetVarsityDesc() {
        return getVarsityDesc;
    }

    public void setGetVarsityDesc(String getVarsityDesc) {
        this.getVarsityDesc = getVarsityDesc;
    }
}
